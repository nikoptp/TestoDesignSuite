const mimeTypeFromExtension = (extension: string): string => {
  const normalized = extension.toLowerCase();
  if (normalized === '.png') {
    return 'image/png';
  }
  if (normalized === '.jpg' || normalized === '.jpeg') {
    return 'image/jpeg';
  }
  if (normalized === '.gif') {
    return 'image/gif';
  }
  if (normalized === '.webp') {
    return 'image/webp';
  }
  if (normalized === '.bmp') {
    return 'image/bmp';
  }
  if (normalized === '.svg') {
    return 'image/svg+xml';
  }
  if (normalized === '.avif') {
    return 'image/avif';
  }
  return 'application/octet-stream';
};

const readProjectBundle = async (filePath: string): Promise<ProjectBundleV1> => {
  const content = await readFile(filePath, 'utf-8');
  const parsed = JSON.parse(content) as Partial<ProjectBundleV1>;
  if (parsed.version !== 1 || !Array.isArray(parsed.images)) {
    throw new Error('Invalid project file format.');
  }
  return {
    version: 1,
    exportedAt: typeof parsed.exportedAt === 'number' ? parsed.exportedAt : Date.now(),
    treeState: parsed.treeState ?? null,
    userSettings: parsed.userSettings ?? null,
    images: parsed.images
      .filter(
        (item) =>
          item &&
          typeof item.relativePath === 'string' &&
          typeof item.mimeType === 'string' &&
          typeof item.dataBase64 === 'string',
      )
      .map((item) => ({
        relativePath: item.relativePath,
        mimeType: item.mimeType,
        dataBase64: item.dataBase64,
      })),
  };
};

const writeProjectBundle = async (filePath: string): Promise<void> => {
  const focusedWindow = BrowserWindow.getFocusedWindow();
  const snapshot = focusedWindow ? await requestRendererProjectSnapshot(focusedWindow) : null;

  const [treeState, userSettings, assets] = await Promise.all([
    snapshot ? Promise.resolve(snapshot.treeState) : loadTreeState(),
    snapshot ? Promise.resolve(snapshot.userSettings) : loadUserSettings(),
    listImageAssets(),
  ]);

  const images: ProjectBundleV1['images'] = [];
  for (const asset of assets) {
    const bytes = await readFile(asset.absolutePath);
    images.push({
      relativePath: asset.relativePath,
      mimeType: mimeTypeFromExtension(path.extname(asset.absolutePath)),
      dataBase64: bytes.toString('base64'),
    });
  }

  const bundle: ProjectBundleV1 = {
    version: 1,
    exportedAt: Date.now(),
    treeState,
    userSettings,
    images,
  };

  await safeWriteFile(filePath, JSON.stringify(bundle));
};

const requestRendererProjectSnapshot = async (
  window: BrowserWindow,
): Promise<ProjectSnapshot | null> => {
  const requestId = nextSnapshotRequestId++;

  return new Promise((resolve) => {
    const timeout = setTimeout(() => {
      const pending = pendingSnapshotRequests.get(requestId);
      if (!pending) {
        return;
      }
      pendingSnapshotRequests.delete(requestId);
      pending.resolve(null);
    }, PROJECT_SNAPSHOT_TIMEOUT_MS);

    pendingSnapshotRequests.set(requestId, {
      senderId: window.webContents.id,
      resolve,
      timeout,
    });

    window.webContents.send('project:request-snapshot', requestId);
  });
};

const applyProjectBundle = async (bundle: ProjectBundleV1): Promise<void> => {
  if (bundle.treeState) {
    await saveTreeState(bundle.treeState);
  }
  if (bundle.userSettings) {
    await saveUserSettings(bundle.userSettings);
  }

  const imageDir = getProjectImageAssetsDir();
  await mkdir(imageDir, { recursive: true });
  const existing = await readdir(imageDir, { withFileTypes: true });
  for (const entry of existing) {
    if (entry.isFile()) {
      await unlink(path.join(imageDir, entry.name));
    }
  }

  for (const image of bundle.images) {
    const normalized = normalizeRelativePath(image.relativePath).replace(/^[/\\]+/, '');
    const resolved = resolveImageAssetPath(normalized);
    if (!resolved) {
      continue;
    }
    const folderPath = path.dirname(resolved);
    await mkdir(folderPath, { recursive: true });
    const bytes = Buffer.from(image.dataBase64, 'base64');
    await writeFile(resolved, bytes);
  }
};

let activeProjectFilePath: string | null = null;

const emitProjectStatus = (
  window: BrowserWindow | null,
  payload: Omit<ProjectStatusPayload, 'at'>,
): void => {
  if (!window || window.isDestroyed()) {
    return;
  }
  const statusPayload: ProjectStatusPayload = {
    ...payload,
    at: Date.now(),
  };
  window.webContents.send('menu:project-status', statusPayload);
};

const saveProjectFileAs = async (): Promise<void> => {
  const focusedWindow = BrowserWindow.getFocusedWindow();
  if (!focusedWindow) {
    return;
  }

  const result = await dialog.showSaveDialog(focusedWindow, {
    title: 'Save Project File',
    defaultPath: activeProjectFilePath ?? path.join(getProjectRootPath(), 'untitled.testo'),
    filters: [{ name: 'Testo Project', extensions: ['testo'] }],
  });
  if (result.canceled || !result.filePath) {
    return;
  }

  try {
    await writeProjectBundle(result.filePath);
    activeProjectFilePath = result.filePath;
    emitProjectStatus(focusedWindow, {
      status: 'success',
      action: 'save-as',
      filePath: result.filePath,
      message: `Saved project as ${path.basename(result.filePath)}.`,
    });
  } catch (error: unknown) {
    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'save-as',
      filePath: result.filePath,
      message: `Save As failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
  }
};

const saveProjectFile = async (): Promise<void> => {
  if (!activeProjectFilePath) {
    await saveProjectFileAs();
    return;
  }
  const focusedWindow = BrowserWindow.getFocusedWindow();
  try {
    await writeProjectBundle(activeProjectFilePath);
    emitProjectStatus(focusedWindow, {
      status: 'success',
      action: 'save',
      filePath: activeProjectFilePath,
      message: `Saved ${path.basename(activeProjectFilePath)}.`,
    });
  } catch (error: unknown) {
    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'save',
      filePath: activeProjectFilePath,
      message: `Save failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
  }
};

const openProjectFile = async (): Promise<void> => {
  const focusedWindow = BrowserWindow.getFocusedWindow();
  if (!focusedWindow) {
    return;
  }

  const result = await dialog.showOpenDialog(focusedWindow, {
    title: 'Open Project File',
    properties: ['openFile'],
    filters: [{ name: 'Testo Project', extensions: ['testo'] }],
  });
  if (result.canceled || result.filePaths.length === 0) {
    return;
  }

  try {
    const filePath = result.filePaths[0];
    const bundle = await readProjectBundle(filePath);
    await applyProjectBundle(bundle);
    activeProjectFilePath = filePath;
    emitProjectStatus(focusedWindow, {
      status: 'success',
      action: 'open',
      filePath,
      message: `Opened ${path.basename(filePath)}.`,
    });
    focusedWindow.reload();
  } catch (error: unknown) {
    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'open',
      message: `Open failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
  }
};

const createNewProject = async (): Promise<void> => {
  const focusedWindow = BrowserWindow.getFocusedWindow();
  if (!focusedWindow) {
    return;
  }

  const result = await dialog.showMessageBox(focusedWindow, {
    type: 'question',
    buttons: ['Create New Project', 'Cancel'],
    defaultId: 1,
    cancelId: 1,
    title: 'Create New Project',
    message: 'Create a new project and clear the current local project data?',
    detail: 'This clears current tree, settings, and cached images for this workspace.',
  });

  if (result.response !== 0) {
    return;
  }

  try {
    await clearCurrentProjectData();
    activeProjectFilePath = null;
    emitProjectStatus(focusedWindow, {
      status: 'success',
      action: 'new',
      filePath: null,
      message: 'Created a new project workspace.',
    });
    focusedWindow.reload();
  } catch (error: unknown) {
    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'new',
      filePath: null,
      message: `New project failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
  }
};

