const extensionFromMimeType = (mimeType: string): string => {
  const normalized = mimeType.toLowerCase();
  if (normalized === 'image/png') {
    return 'png';
  }
  if (normalized === 'image/jpeg' || normalized === 'image/jpg') {
    return 'jpg';
  }
  if (normalized === 'image/gif') {
    return 'gif';
  }
  if (normalized === 'image/webp') {
    return 'webp';
  }
  if (normalized === 'image/bmp') {
    return 'bmp';
  }
  if (normalized === 'image/svg+xml') {
    return 'svg';
  }
  if (normalized === 'image/avif') {
    return 'avif';
  }

  return 'png';
};

const fileExists = async (filePath: string): Promise<boolean> => {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
};

const createBackupPath = (filePath: string): string => {
  const parsed = path.parse(filePath);
  return path.join(getDataBackupDir(), `${parsed.base}.${Date.now()}.bak`);
};

const maybeBackupFile = async (filePath: string): Promise<void> => {
  if (!(await fileExists(filePath))) {
    return;
  }
  const backupDir = getDataBackupDir();
  await mkdir(backupDir, { recursive: true });
  const backupPath = createBackupPath(filePath);
  await copyFile(filePath, backupPath);
};

const safeWriteFile = async (filePath: string, content: string | Uint8Array): Promise<void> => {
  const folderPath = path.dirname(filePath);
  await mkdir(folderPath, { recursive: true });
  await maybeBackupFile(filePath);
  const tempPath = `${filePath}.tmp`;
  await writeFile(tempPath, content);
  await rename(tempPath, filePath);
};

const findNewestBackup = async (filePath: string): Promise<string | null> => {
  const backupDir = getDataBackupDir();
  if (!(await fileExists(backupDir))) {
    return null;
  }
  const base = path.parse(filePath).base;
  const entries = await readdir(backupDir, { withFileTypes: true });
  const candidates = entries
    .filter((entry) => entry.isFile() && entry.name.startsWith(`${base}.`) && entry.name.endsWith('.bak'))
    .map((entry) => path.join(backupDir, entry.name));
  if (candidates.length === 0) {
    return null;
  }
  let newest: { filePath: string; mtime: number } | null = null;
  for (const candidate of candidates) {
    const metadata = await stat(candidate);
    if (!newest || metadata.mtimeMs > newest.mtime) {
      newest = {
        filePath: candidate,
        mtime: metadata.mtimeMs,
      };
    }
  }
  return newest?.filePath ?? null;
};

const loadJsonWithBackup = async <T>(filePath: string): Promise<T | null> => {
  try {
    const contents = await readFile(filePath, 'utf-8');
    return JSON.parse(contents) as T;
  } catch (error: unknown) {
    if (
      typeof error === 'object' &&
      error !== null &&
      'code' in error &&
      (error as { code?: string }).code === 'ENOENT'
    ) {
      return null;
    }

    const backupPath = await findNewestBackup(filePath);
    if (!backupPath) {
      throw error;
    }

    const backupContents = await readFile(backupPath, 'utf-8');
    return JSON.parse(backupContents) as T;
  }
};

const isImageFileName = (fileName: string): boolean => {
  const extension = path.extname(fileName).toLowerCase().slice(1);
  return IMAGE_EXTENSIONS.includes(extension);
};

const toAssetUrl = (relativePath: string): string => {
  const encodedRelativePath = normalizeRelativePath(relativePath)
    .split('/')
    .map((segment) => encodeURIComponent(segment))
    .join('/');
  return `testo-asset://${encodedRelativePath}`;
};

const resolveImageAssetPath = (relativePath: string): string | null => {
  const projectRoot = getProjectRootPath();
  const imageRoot = path.resolve(getProjectImageAssetsDir());
  const absolutePath = path.resolve(projectRoot, relativePath);
  const relativeFromImageRoot = path.relative(imageRoot, absolutePath);
  if (
    !relativeFromImageRoot ||
    relativeFromImageRoot.startsWith('..') ||
    path.isAbsolute(relativeFromImageRoot)
  ) {
    return null;
  }
  return absolutePath;
};

const saveImageAsset = async (input: {
  bytes: Uint8Array;
  mimeType: string;
}): Promise<SavedImageAsset> => {
  if (!input || !(input.bytes instanceof Uint8Array) || input.bytes.length === 0) {
    throw new Error('Invalid image payload.');
  }

  const imageDir = getProjectImageAssetsDir();
  await mkdir(imageDir, { recursive: true });

  const buffer = Buffer.from(input.bytes);
  const hash = createHash('sha256').update(buffer).digest('hex');
  const desiredExt = extensionFromMimeType(input.mimeType);

  let existingFileName: string | null = null;
  for (const ext of IMAGE_EXTENSIONS) {
    const candidate = `${hash}.${ext}`;
    const candidatePath = path.join(imageDir, candidate);
    if (await fileExists(candidatePath)) {
      existingFileName = candidate;
      break;
    }
  }

  const fileName = existingFileName ?? `${hash}.${desiredExt}`;
  const absolutePath = path.join(imageDir, fileName);

  if (!existingFileName) {
    try {
      await writeFile(absolutePath, buffer, { flag: 'wx' });
    } catch (error: unknown) {
      if (
        typeof error === 'object' &&
        error !== null &&
        'code' in error &&
        (error as { code?: string }).code !== 'EEXIST'
      ) {
        throw error;
      }
    }
  }

  const relativePath = normalizeRelativePath(path.relative(process.cwd(), absolutePath));

  return {
    absolutePath,
    relativePath,
    assetUrl: toAssetUrl(relativePath),
    fileUrl: pathToFileURL(absolutePath).toString(),
    deduplicated: Boolean(existingFileName),
  };
};

