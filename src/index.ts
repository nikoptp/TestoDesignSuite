const openSettingsFromMenu = (): void => {
  const focusedWindow = BrowserWindow.getFocusedWindow();
  if (!focusedWindow) {
    return;
  }
  focusedWindow.webContents.send('menu:open-settings');
};

const createWindow = (): BrowserWindow => {
  return createMainWindow({
    windowEntry: MAIN_WINDOW_WEBPACK_ENTRY,
    preloadEntry: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
    windowIconPath: getWindowIconPath(),
  });
};

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on('ready', () => {
  applyMacDockIcon(getWindowIconPath());
  configureAutoUpdates();

  void (async () => {
    try {
      await ensureWritableWorkspaceRoot();
      const launchState = await getLaunchState();
      activeProjectFilePath = launchState.lastActiveProjectPath;
    } catch {
      activeProjectFilePath = null;
    }
    registerAssetProtocol();
    buildAppMenu({
      onNewProject: () => {
        void createNewProject();
      },
      onOpenProjectFile: () => {
        void openProjectFile();
      },
      onSaveProjectFile: () => {
        void saveProjectFile();
      },
      onSaveProjectFileAs: () => {
        void saveProjectFileAs();
      },
      onOpenSettings: openSettingsFromMenu,
      onCheckForUpdates: () => {
        void checkForGithubUpdates(true);
      },
    });
    mainWindowRef = createWindow();
    mainWindowRef.webContents.once('did-finish-load', () => {
      if (!mainWindowRef || mainWindowRef.isDestroyed()) {
        return;
      }
      openPendingProjectPath(mainWindowRef);
    });
    setTimeout(() => {
      void checkForGithubUpdates(false);
    }, 1200);
  })();
});

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    mainWindowRef = createWindow();
    mainWindowRef.webContents.once('did-finish-load', () => {
      if (!mainWindowRef || mainWindowRef.isDestroyed()) {
        return;
      }
      openPendingProjectPath(mainWindowRef);
    });
  }
});

