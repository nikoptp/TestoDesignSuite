const exportSteamAchievementSet = async (
  request: SteamAchievementExportRequest,
): Promise<SteamAchievementExportResult> => {
  const focusedWindow = BrowserWindow.getFocusedWindow() ?? mainWindowRef;
  if (!focusedWindow) {
    return {
      canceled: true,
      outputDir: null,
      exportedEntryCount: 0,
      skippedEntryCount: 0,
      writtenFileCount: 0,
    };
  }

  const presetLabel = request.data.presetId || 'steam-assets';
  const safeNodeName =
    [...request.nodeName.trim()]
      .map((character) =>
        character.charCodeAt(0) < 32 || /[<>:"/\\|?*]/.test(character) ? '-' : character,
      )
      .join('')
      .trim() || 'steam-achievements';
  const result = await dialog.showOpenDialog(focusedWindow, {
    title: 'Choose Export Folder',
    defaultPath: path.join(getProjectRootPath(), `${safeNodeName}-${presetLabel}`),
    properties: ['openDirectory', 'createDirectory'],
  });
  if (result.canceled || result.filePaths.length === 0) {
    return {
      canceled: true,
      outputDir: null,
      exportedEntryCount: 0,
      skippedEntryCount: 0,
      writtenFileCount: 0,
    };
  }

  const outputDir = result.filePaths[0];
  const normalizedRequest = {
    ...request,
    data: normalizeSteamAchievementArtData(request.data),
  };

  try {
    const exportResult = await writeSteamAchievementExportSet(
      {
        ...normalizedRequest,
        outputDir,
      },
      {
        resolveImageAssetPath,
        renderEntry: (entry, absolutePath) =>
          renderSteamAchievementEntryPngs(
            entry,
            absolutePath,
            normalizedRequest,
            resolveImageAssetPath,
          ),
      },
    );

    emitProjectStatus(focusedWindow, {
      status: exportResult.exportedEntryCount > 0 ? 'success' : 'info',
      action: 'export',
      filePath: outputDir,
      message:
        exportResult.exportedEntryCount > 0
          ? `Exported ${exportResult.exportedEntryCount} achievement image set${exportResult.exportedEntryCount === 1 ? '' : 's'} to ${path.basename(outputDir)}${exportResult.skippedEntryCount > 0 ? `; skipped ${exportResult.skippedEntryCount} invalid entr${exportResult.skippedEntryCount === 1 ? 'y' : 'ies'}` : ''}.`
          : `No achievement images were exported to ${path.basename(outputDir)}.`,
    });

    return exportResult;
  } catch (error: unknown) {
    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'export',
      filePath: outputDir,
      message: `Achievement export failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
    return {
      canceled: false,
      outputDir,
      exportedEntryCount: 0,
      skippedEntryCount: request.data.entries.length,
      writtenFileCount: 0,
    };
  }
};

