const FALLBACK_UPDATE_REPO_SLUG = 'nikoptp/TestoDesignSuite';
const UPDATE_REPO_SLUG = (process.env.TESTO_UPDATE_REPO ?? FALLBACK_UPDATE_REPO_SLUG).trim();
const DEFAULT_UPDATE_INTERVAL = '30 minutes';

const configureAutoUpdates = (): void => {
  if (!app.isPackaged) {
    return;
  }
  if (process.platform === 'win32') {
    return;
  }
  if (!UPDATE_REPO_SLUG) {
    return;
  }

  try {
    updateElectronApp({
      updateSource: {
        type: UpdateSourceType.ElectronPublicUpdateService,
        repo: UPDATE_REPO_SLUG,
      },
      updateInterval: DEFAULT_UPDATE_INTERVAL,
      notifyUser: true,
      logger: console,
    });
  } catch (error) {
    console.error(
      `Failed to configure automatic updates: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
};

const checkForGithubUpdates = async (manual: boolean): Promise<void> => {
  const currentVersion = normalizeVersionTag(app.getVersion());
  const focusedWindow = BrowserWindow.getFocusedWindow() ?? mainWindowRef;

  if (!UPDATE_REPO_SLUG) {
    if (manual) {
      emitProjectStatus(focusedWindow, {
        status: 'info',
        action: 'update',
        filePath: null,
        message:
          'Updates are not configured. Set TESTO_UPDATE_REPO to your GitHub repo (owner/name).',
      });
    }
    return;
  }

  try {
    const latest = await fetchLatestGithubRelease(
      UPDATE_REPO_SLUG,
      `${app.getName()}/${app.getVersion()}`,
    );
    const comparison = compareVersions(latest.latestVersion, currentVersion);

    if (comparison > 0) {
      emitProjectStatus(focusedWindow, {
        status: 'info',
        action: 'update',
        filePath: null,
        message: `Update available: v${latest.latestVersion} (current v${currentVersion}).`,
        updatePhase: 'available',
      });

      if (manual && focusedWindow) {
        const canRunWindowsInstallerUpdate = process.platform === 'win32' && app.isPackaged;
        const installerAsset = canRunWindowsInstallerUpdate
          ? pickWindowsInstallerAsset(latest.assets)
          : null;

        if (canRunWindowsInstallerUpdate && installerAsset) {
          const checksumAsset = pickChecksumAssetForInstaller(latest.assets, installerAsset);
          const result = await dialog.showMessageBox(focusedWindow, {
            type: 'info',
            buttons: ['Install Update', 'Open Download Page', 'Later'],
            defaultId: 0,
            cancelId: 2,
            title: 'Update Available',
            message: `Version ${latest.latestTag} is available.`,
            detail:
              `Current version: v${currentVersion}\n` +
              `Latest version: v${latest.latestVersion}\n` +
              `${checksumAsset ? 'Checksum verification: available' : 'Checksum verification: unavailable'}`,
          });

          if (result.response === 0) {
            try {
              emitProjectStatus(focusedWindow, {
                status: 'info',
                action: 'update',
                filePath: null,
                message: `Downloading installer (${installerAsset.name})...`,
                updatePhase: 'downloading',
                progressMode: 'indeterminate',
              });

              const updaterDir = path.join(app.getPath('userData'), 'updater');
              const downloadedInstaller = await downloadReleaseAsset({
                asset: installerAsset,
                userAgent: `${app.getName()}/${app.getVersion()}`,
                destinationDir: updaterDir,
              });

              if (checksumAsset) {
                emitProjectStatus(focusedWindow, {
                  status: 'info',
                  action: 'update',
                  filePath: null,
                  message: `Verifying installer checksum (${checksumAsset.name})...`,
                  updatePhase: 'verifying',
                  progressMode: 'indeterminate',
                });

                const downloadedChecksum = await downloadReleaseAsset({
                  asset: checksumAsset,
                  userAgent: `${app.getName()}/${app.getVersion()}`,
                  destinationDir: updaterDir,
                });
                await verifyDownloadedInstallerChecksum(
                  downloadedInstaller.localPath,
                  downloadedChecksum.localPath,
                );
              }

              emitProjectStatus(focusedWindow, {
                status: 'info',
                action: 'update',
                filePath: null,
                message: 'Installing update and restarting...',
                updatePhase: 'installing',
                progressMode: 'indeterminate',
              });

              await launchSilentWindowsInstaller(
                downloadedInstaller.localPath,
                app.getPath('exe'),
              );
              app.quit();
            } catch (error: unknown) {
              emitProjectStatus(focusedWindow, {
                status: 'error',
                action: 'update',
                filePath: null,
                message:
                  `Automatic install failed: ` +
                  `${error instanceof Error ? error.message : 'Unknown error'}`,
              });
            }
          } else if (result.response === 1) {
            await shell.openExternal(latest.downloadUrl);
          }
        } else {
          const result = await dialog.showMessageBox(focusedWindow, {
            type: 'info',
            buttons: ['Open Download Page', 'Later'],
            defaultId: 0,
            cancelId: 1,
            title: 'Update Available',
            message: `Version ${latest.latestTag} is available.`,
            detail: `Current version: v${currentVersion}\nLatest version: v${latest.latestVersion}`,
          });

          if (result.response === 0) {
            await shell.openExternal(latest.downloadUrl);
          }
        }
      }
      return;
    }

    if (manual) {
      emitProjectStatus(focusedWindow, {
        status: 'success',
        action: 'update',
        filePath: null,
        message: `You're on the latest version (v${currentVersion}).`,
      });
    }
  } catch (error: unknown) {
    if (!manual) {
      return;
    }

    emitProjectStatus(focusedWindow, {
      status: 'error',
      action: 'update',
      filePath: null,
      message: `Update check failed: ${error instanceof Error ? error.message : 'Unknown error'}`,
    });
  }
};

